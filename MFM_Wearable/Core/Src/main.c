static float Convert_Temperature(float Vin) {
    const float Vref = 1.25f;       // AD8495 0°C 오프셋
    const float sensitivity = 0.005f; // 5 mV/°C
    float Temp_Celsius = (Vin - Vref) / sensitivity + 3.9f;
    return Temp_Celsius;
}
